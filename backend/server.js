app.post("/translate", (req, res) => {
  const { text } = req.body;
  if (!text) return res.json({ emoji: "" });

  let words = text.toLowerCase().split(/\s+/);
  let result = "";

  words.forEach((word) => {
    if (emojiMap[word]) {
      result += emojiMap[word] + " ";
    } else {
      result += "â“ "; // fallback
    }
  });

  res.json({ emoji: result.trim() });
});

